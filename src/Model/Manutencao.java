package Model;
public class Manutencao {
    private String descricao;
    private double custoassoc;
    private double hora;
    private double dataInic;
    
    public String getDescricao() {
        return descricao;
    }
    public void setDescricao(String descricao) {
        this.descricao = descricao;
    }
    public double getCustoassoc() {
        return custoassoc;
    }
    public void setCustoassoc(double custoassoc) {
        this.custoassoc = custoassoc;
    }
    public double getHora() {
        return hora;
    }
    public void setHora(double hora) {
        this.hora = hora;
    }
    public double getDataInic() {
        return dataInic;
    }
    public void setDataInic(double dataInic) {
        this.dataInic = dataInic;
    }

    

}
